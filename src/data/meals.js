// Add more meals as needed...
export const getMealsByRegion = (region) => {
  return MEALS.filter(meal => meal.region === region);
};

export const getMealsByHealthTag = (tag) => {
  return MEALS.filter(meal => meal.healthTags.includes(tag));
};

export const getMealById = (id) => {
  return MEALS.find(meal => meal.id === id);
};

export const getAllRegions = () => {
  return [...new Set(MEALS.map(meal => meal.region))];
};

export const getAllHealthTags = () => {
  return [...new Set(MEALS.flatMap(meal => meal.healthTags))];
};
